package ut3.calculadora;

/**
 * This class is intended to calculate basic operations between two integer numbers a and b
 *
 * @author francis
 */
public class CalculadoraBasica {

    /**
     *
     * @param a the first operand
     * @param b the second operand
     * @return the result of a + b
     */
    public static Integer suma (Integer a, Integer b) {
        return a + b;
    }

    static Integer resta (Integer a, Integer b) { return a - b;}
}
